#ifndef __MOTOR_H
#define __MOTOR_H

// ============================================================
// TB6612 电机驱动模块
//   通过4个方向引脚 + 2个PWM引脚控制两个直流电机
//
// 接线对照表：
//   STM32  →  TB6612
//   ------     ------
//   PB5    →   AIN1 (左电机方向1)
//   PB6    →   AIN2 (左电机方向2)
//   PB7    →   BIN1 (右电机方向1)
//   PB8    →   BIN2 (右电机方向2)
//   PA7    →   PWMA (左电机速度)  TIM3_CH2
//   PA6    →   PWMB (右电机速度)  TIM3_CH1
// ============================================================

void Motor_GPIO_Init(void);   // 初始化电机控制引脚

// 设置左右轮速度，范围 -7200 ~ +7200
//   正数 = 前进    负数 = 后退    0 = 刹车
void Motor_SetSpeed(int leftSpeed, int rightSpeed);

#endif
